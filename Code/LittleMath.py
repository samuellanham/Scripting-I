print("This program does math.")
num1 = int(input("Number 1: "))
num2 = int(input("Number 2: "))
print()
num3 = num1 + num2
print(num1, "+", num2, "=", num3)
num3 = num1 - num2
print(num1, "-", num2, "=", num3)
num3 = num1 * num2
print(num1, "*", num2, "=", num3)
num3 = num1 / num2
print(num1, "/", num2, "=", format(num3, ".2f")) 
num3 = num1 // num2
print(num1, "//", num2, "=", num3)
num3 = num1 ** num2
print(num1, "^", num2, "=", num3)
